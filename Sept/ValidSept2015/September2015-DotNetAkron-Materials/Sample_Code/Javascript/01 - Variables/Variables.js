console.log("JavaScript has strings:");
var myString = "Hello World";
console.log(myString + "\n");

console.log("JavaScript has numbers:");
var myFirstNumber = 1;
console.log(myFirstNumber + "\n");

console.log("Which by the way are more like doubles, notice it doesn't care if I add some decimal:");
var myFirstNumber = 1.5;
console.log(myFirstNumber + "\n");

console.log("JavaScript has booleans too:");
var myBool = true;
console.log(myBool + "\n");

console.log("JavaScript is a dynamic-typed language, thus I can just go ahead and put a number in myString: ");
myString = 5;
console.log(myString + "\n");
